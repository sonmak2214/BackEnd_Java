package patternProgs;
public class ColumnDigit 
{
 public static void main(String[] args) 
 {
	for(int i=1;i<=5;i++) //6<=5
	{
		for(int j=1;j<=5;j++)//6<=5
		{
			System.out.print(j+" ");
		}
		System.out.println();
	}
 }
}

package wrapping;

public class Unboxing
{

 public static void main(String[] args) 
 {
 Integer obj=new Integer(10);//object type data
 int n1=obj;//unboxing--convert objctype data into primitive data
 System.out.println(n1);//conversion from object to primitive is known as Unboxing.. 
 int n2=20;
 System.out.println(n2);
 }

}
